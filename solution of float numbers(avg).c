
#include<stdio.h>
int main()
{
    int a,b,sum;
    float avg;
    printf("Enter Two Number=");
    scanf("%d%d",&a,&b);
    sum=a+b;
    avg= (float)sum/2;
    printf("Result is=%d\n%f",sum,avg);

    return 0;
}
//SUM IS AN INTEGAR NUMBER AND INTEGER divided by INTEGER is equal to INTEGER THAT IS WHY SUM IS DECLARED IN FLOAT THEN AVG WILL SHOW FLOAT NUMBER//


